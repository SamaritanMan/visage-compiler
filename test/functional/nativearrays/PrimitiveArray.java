public class PrimitiveArray {
    public static long longArraySum(long[] array) {
        long sum = 0;
        for (int i = array.length;  --i >= 0; )
            sum += array[i];
        return sum;
    }

    public static int intArraySum(int[] array) {
        int sum = 0;
        for (int i = array.length;  --i >= 0; )
            sum += array[i];
        return sum;
    }

    public static float floatArraySum(float[] array) {
        float sum = 0;
        for (int i = array.length;  --i >= 0; )
            sum += array[i];
        return sum;
    }

    public static double doubleArraySum(float[] array) {
        double sum = 0;
        for (int i = array.length;  --i >= 0; )
            sum += array[i];
        return sum;
    }
} 
