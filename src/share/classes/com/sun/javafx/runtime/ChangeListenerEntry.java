package com.sun.javafx.runtime;

/**
 * An element in the set of listeners registered with a location.
 * The set of listeners than an object should notify is implemented
 * as a linked list, where each link implements ChangeListenerEntry.
 * (ChangeListenerEntry is an interface so links can extend WeakRef.)
 * 
 * @author Per Bothner
 */
public interface ChangeListenerEntry {
  void changed ();

  public ChangeListenerEntry getNext ();
  public void setNext (ChangeListenerEntry next);
}
